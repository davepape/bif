<h2>What is it?</h2>
<p>The buffalo “infringement” festival is a non-profit-driven, non-hierarchical grassroots endeavor bringing together a broad range of eclectic, independent, experimental, and controversial art of all forms. Visual, performing, musical, and media arts are all welcome here. Taking place in multiple venues in and around Buffalo’s Allentown District, the festival is an annual eleven-day event running from the last weekend of July through the first weekend of August. 
</p>
<p>See the Infringement Festival International's <a href="http://www.infringementfestival.com/eng/mandate.html">mandate</a>, and our <a href='missionstatement.php'>2006 Mission Statement</a>, to learn more.
</p>
